int firstNumber = 5;
int secondNumber = 20;

if (firstNumber == secondNumber)
{
    Console.WriteLine("числа равны");
}
else
{
    if (firstNumber < secondNumber)
    {
        Console.Write("первое число меньше второго");
    }
    else
    {
        Console.Write("первое число больше второго");
    }
}
